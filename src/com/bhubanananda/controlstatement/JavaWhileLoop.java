package com.bhubanananda.controlstatement;

/**
 * The Class JavaWhileLoop.
 *
 * @author Bhubanananda
 */
public class JavaWhileLoop {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}
	}

}
