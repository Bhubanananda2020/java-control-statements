package com.bhubanananda.controlstatement;

/**
 * The Class JavaDoWhileLoop.
 *
 * @author Bhubanananda
 */
public class JavaDoWhileLoop {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {

		int i = 0;

		do {
			System.out.println(i);
			i++;
		} while (i <= 10);

	}

}
